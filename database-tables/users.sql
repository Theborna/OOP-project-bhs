CREATE TABLE `electron`.`users` (
  `US_ID` BIGINT(32) NOT NULL AUTO_INCREMENT,
  `US_USName` VARCHAR(32) NOT NULL,
  `US_Pass_enc` VARCHAR(64) NOT NULL,
  `US_Salt` VARCHAR(8) NOT NULL,
  `US_Date_Join` DATETIME NOT NULL,
  `US_Type` INT NOT NULL,
  `US_Visibilty` INT NOT NULL,
  `US_Followers_Count` INT NOT NULL,
  `US_Following_Count` INT NOT NULL,
  `US_Post_Count` INT NOT NULL,
  `US_Name` VARCHAR(45) NOT NULL,
  `US_LastName` VARCHAR(45) NULL DEFAULT NULL,
  `US_Email` LONGTEXT NOT NULL,
  `US_PromotionIndex` DOUBLE NULL DEFAULT NULL,
  `US_secType` INT NOT NULL,
  `US_SecQuiz` VARCHAR(64) NOT NULL,
  `us_pic_id` BIGINT(32) NULL DEFAULT 0,
